package com.company;


public class Main {

    public static void main(String[] args) {
        char switchValue = 'a';
        switch (switchValue) {
            case 'a':
                System.out.println("value is mumbai");
                break;
            case 'b':
                System.out.println("city is chennai");
                break;
            default:
                System.out.println("none of them");
                break;

        }
    }
}




